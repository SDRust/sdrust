//! San Diego Rust is dedicated to providing a harassment-free conference experience for everyone, regardless of gender, gender identity and expression, sexual orientation, disability, physical appearance, body size, race, age or religion. We do not tolerate harassment of conference participants in any form. Sexual language and imagery is not appropriate for any meetup venue, including talks. Meetup participants violating these rules may be expelled from the meetup at the discretion of the organizers.
//!
//! Harassment includes, but is not limited to:
//!
//!  * Verbal comments that reinforce social structures of domination [related to gender, gender identity and expression, sexual orientation, disability, physical appearance, body size, race, age, religion, [your specific concern here].
//!  * Sexual images in public spaces
//!  * Deliberate intimidation, stalking, or following 
//!  * Harassing photography or recording
//!  * Sustained disruption of talks or other events
//!  * Inappropriate physical contact
//!  * Unwelcome sexual attention
//!  * Advocating for, or encouraging, any of the above behaviour
//!
//! <h2>Enforcement:</h2>
//!
//! Participants asked to stop any harassing behavior are expected to comply immediately
//!
//! If a participant engages in harassing behaviour, event organisers retain the right to take any actions to keep the event a welcoming environment for all participants. This includes warning the offender or expulsion from the meetup.
//!
//! Event organisers may take action to redress anything designed to, or with the clear impact of, disrupting the event or making the environment hostile for any participants.
//!
//! We expect participants to follow these rules at all event venues and event-related social activities. We think people should follow these rules outside event activities too!
//! 
//! <h2>Reporting</h2>
//!
//! If someone makes you or anyone else feel unsafe or unwelcome, please report it as soon as possible. Harassment and other code of conduct violations reduce the value of our event for everyone. We want you to be happy at our event. People like you make our event a better place.
//!
//! You can make a report either personally or anonymously.
//!
//! <h2>Anonymous Report</h2>
//!
//! You can make an anonymous report here [Wufoo form or similar that DOES NOT require an email address to submit. <http://www.wufoo.com/>]
//!
//! We can't follow up an anonymous report with you directly, but we will fully investigate it and take whatever action is necessary to prevent a recurrence.
//!
